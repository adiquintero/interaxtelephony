package com.interax.telephony.service.exception;

public abstract class InteraxTelephonyException extends Exception{
	private static final long serialVersionUID = 1L;

	public InteraxTelephonyException(String message) {
		super(message);
	}
    
    public InteraxTelephonyException(String message,StackTraceElement[] stackTrace) {
        super(message);
        setStackTrace(stackTrace);
    }
    
    public InteraxTelephonyException(Exception e) {
    	super(e.getMessage(), e.getCause());
    	this.setStackTrace(e.getStackTrace());
    }
}